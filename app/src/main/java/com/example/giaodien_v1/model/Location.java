package com.example.giaodien_v1.model;

public class Location {
    double v1;
    double v2;

    public Location() {
    }

    public Location(double v1, double v2) {
        this.v1 = v1;
        this.v2 = v2;
    }

    public double getV1() {
        return v1;
    }

    public void setV1(double v1) {
        this.v1 = v1;
    }

    public double getV2() {
        return v2;
    }

    public void setV2(double v2) {
        this.v2 = v2;
    }
}
